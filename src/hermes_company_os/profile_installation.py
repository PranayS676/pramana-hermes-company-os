from __future__ import annotations

import json
from collections import Counter

from hermes_company_os.hermes_paths import (
    powershell_hermes_home_param,
    powershell_profile_root_label,
)

IDENTITY_FILES = ["SOUL.md", "capabilities.json", "profile-manifest.json"]
STARTER_FILES = [".env.example", "config.yaml.example"]
LIVE_FILES = [".env", "config.yaml"]


def profile_installation_payload(
    *,
    agents: list[dict],
    profile_installation_checks: list[dict] | None = None,
) -> dict:
    checks = profile_installation_checks or []
    return {
        "title": "Hermes Profile Installation Audit",
        "credential_boundary": (
            "This audit checks profile directories, expected identity files, starter "
            "files, and command aliases only. It does not read, print, store, or "
            "send live `.env` values, provider API keys, Slack tokens, Telegram bot "
            "tokens, OAuth payloads, request headers, private endpoints, or logs."
        ),
        "profile_root": powershell_profile_root_label(),
        "identity_files": IDENTITY_FILES,
        "starter_files": STARTER_FILES,
        "live_files_checked_for_presence_only": LIVE_FILES,
        "profiles": [_profile(agent) for agent in agents],
        "tracking": {
            "status": _status_counts(checks),
            "checks": [_tracking_check(check) for check in checks],
        },
        "entry_points": {
            "bootstrap": "/setup/bootstrap.ps1",
            "profile_artifacts": "/setup/profile-artifacts.md",
            "profile_installation_tracking": "/setup#profile-installation-tracking",
            "runtime_preflight": "/setup/runtime-preflight.md",
            "gateway_operations": "/setup/gateway-operations.md",
            "credential_loading": "/setup/credential-loading.md",
        },
    }


def profile_installation_json(**kwargs) -> str:
    return (
        json.dumps(profile_installation_payload(**kwargs), indent=2, sort_keys=True)
        + "\n"
    )


